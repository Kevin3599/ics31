# 各种快捷运算符示例
num1 = 10
num2 = 3

# 加法快捷方式
num1 += num2      # 等同于 num1 = num1 + num2
print("加法:", num1)

# 减法快捷方式
num1 -= num2      # 等同于 num1 = num1 - num2
print("减法:", num1)

# 乘法快捷方式
num1 *= num2      # 等同于 num1 = num1 * num2
print("乘法:", num1)

# 除法快捷方式
num1 /= num2      # 等同于 num1 = num1 / num2
print("除法:", num1)

# 取余快捷方式
num1 %= num2      # 等同于 num1 = num1 % num2
print("取余:", num1)

# 幂运算快捷方式
num1 **= num2     # 等同于 num1 = num1 ** num2
print("幂运算:", num1)

# 整除快捷方式
num1 //= num2     # 等同于 num1 = num1 // num2
print("整除:", num1)
